async function seedEvents() {
  console.log('ğŸŒ± Starting event seeding...\n');

  try {
    // First, clear existing events
    console.log('ğŸ—‘ï¸  Clearing existing events...');
    await prisma.event.deleteMany({});
    console.log('âœ… Existing events cleared\n');

    // Insert events
    console.log('ğŸ“ Creating events...\n');
    let successCount = 0;
    let errorCount = 0;

    for (const event of events) {
      try {
        await prisma.event.create({
          data: event,
        });
        console.log(`âœ… Created: ${event.title} (${event.eventId})`);
        successCount++;
      } catch (error) {
        console.error(`âŒ Failed to create ${event.eventId}:`, error);
        errorCount++;
      }
    }

    console.log('\nğŸ“Š Seeding Summary:');
    console.log(`âœ… Successfully created: ${successCount} events`);
    console.log(`âŒ Failed: ${errorCount} events`);
    console.log(`ğŸ“ˆ Total: ${events.length} events`);

  } catch (error) {
    console.error('âŒ Seeding failed:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

